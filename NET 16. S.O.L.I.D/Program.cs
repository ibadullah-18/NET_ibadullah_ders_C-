/*
                        S.O.L.I.D.

1. Single Responsibility Principle (SRP) vahid mesuliyyet prinsipi:
    - Classda desiklik elemek ucun yalniz bir sebep olmalidir.

2. Open/Closed Principle (OCP) acli/qapali prinsipi:
    - Classlar yeni funksionalliq elave etmek ucun acli, amma deyiwikliklere qapali olmalidir.

3. Liskov Substitution Principle (LSP) Liskov deyiwdirme prinsipi:
    - Bir classdan miras alan classlar, bazali classin funksionalliqini deyiwdirme ve ya pozma olmadan istifade edilmelidir.

4. Interface Segregation Principle (ISP) interfeys ayirma prinsipi:
    - Interfeysler spesifik funksionalliqa malik olmalidir, yəni interfeysler coxlu metodlar ile dolu olmamalidir.

5. Dependency Inversion Principle (DIP) asiliq deyiwdirme prinsipi:
    - Yuxari seviyeli modullar asagi seviyeli modullara asili olmamalidir, her ikisi de abstraksiyalara asili olmalidir. Abstraksiyalar konkret implementasiyalardan asili olmamalidir.
 
